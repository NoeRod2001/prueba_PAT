public class SentenciaIfElse_29 {

    public static void main(String[] args) {

        //---------------videos 51---------------//

        float promedio = 19.3f;

        if(promedio >= 18.0){

            System.out.println("Felicidades, estás en el top de los promedios :D");

        }else if(promedio >= 15.0){

            System.out.println("Es buen promedio, sigue así :)");

        }else if(promedio >= 12.0){

            System.out.println("No es excelente pero debes esforzarte");

        }else if(promedio >= 10){

            System.out.println("Necesitas esforzarte más para mejorar");

        }else{

            System.out.println("Insuficiente, deja de estar flojeando y estudia");

        }

        System.out.println("Tu promedio es = " + promedio);

    }

}
